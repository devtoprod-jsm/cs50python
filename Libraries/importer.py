"""Module for printing hello and bye"""


def hello(name):
    """Function for printing"""
    print(f"Hello {name}")


def bye(name):
    """Function to print bye"""
    print(f"Bye {name}")


def main(name):
    """Function to print hello and bye"""
    hello(name)
    bye(name)


if __name__ == "__main__":
    main("Jai")
